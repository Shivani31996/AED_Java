    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(validateThis())
        {
            if(restaurant.getMenu().checkIfFoodItemIsUnique(txtName.getText()))
            {
                FoodItem fi = restaurant.getMenu().createNewFoodItem(txtName.getText(), (Integer)jSpinPrice.getValue());
                JOptionPane.showMessageDialog(null, "Food Item " + fi.getName()+ " created successfully!");
                populateTable();
                btnCreate.setEnabled(true);
                txtName.setText("");
                txtName.setEnabled(false);
                jSpinPrice.setValue(0);
                jSpinPrice.setEnabled(false);
                btnSubmit.setEnabled(false);
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Food item already exists!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        else
        {
            return;
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

