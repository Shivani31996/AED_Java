    private void populateMenu() {
        DefaultTableModel dtm = (DefaultTableModel)tblMenu.getModel();
        dtm.setRowCount(0);
        if(restaurant.getMenu().getFoodItemList() != null)
        {
            for(FoodItem fi : restaurant.getMenu().getFoodItemList())
            {
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = fi.getId();
                row[1] = fi;
                row[2] = fi.getPrice();
                dtm.addRow(row);
            }
        }
        if(dtm.getRowCount() == 0)
            {
                btnAddItem.setEnabled(false);
                btnDelete.setEnabled(false);
            }
    }

    private void populateOrder() {
        DefaultTableModel dtm = (DefaultTableModel)tblOrder.getModel();
        dtm.setRowCount(0);
        if(cart != null)
        {
            btnDelete.setEnabled(true);
            btnOrder.setEnabled(true);
            int count = 1;
            for(FoodItem fi3 : cart)
            {
                Object[] row = new Object[dtm.getColumnCount()];
                row[0] = count;
                row[1] = fi3;
                row[2] = fi3.getPrice();
                dtm.addRow(row);
                count++;
            }
            txtAmount.setText(Integer.toString(totalAmount));
        }
        if(tblOrder.getRowCount() <= 0)
        {
            btnOrder.setEnabled(false);
            btnDelete.setEnabled(false);
        }
    }
