    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblMenu.getSelectedRow();
        if (selectedRow >= 0)
        {
            FoodItem fi2 = (FoodItem) tblMenu.getValueAt(selectedRow, 1);
            //order = restaurant.getOrderDirectory().createNewOrder(customer);
            //order.addItem(fi);
            cart.add(fi2);
            JOptionPane.showMessageDialog(null, "Food Item " + fi2.getName()+ " added to cart successfully!");
            totalAmount = totalAmount + fi2.getPrice();
            populateOrder();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnAddItemActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow1 = tblOrder.getSelectedRow();
        if (selectedRow1 >= 0)
        {
            FoodItem fi1 = (FoodItem) tblOrder.getValueAt(selectedRow1, 1);
            //order.deleteFoodItem(fi);
            cart.remove(fi1);
            JOptionPane.showMessageDialog(null, "Food Item " + fi1.getName()+ " deleted from cart successfully!");
            totalAmount = totalAmount - fi1.getPrice();
            populateOrder();
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select a row!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        // TODO add your handling code here:
        order = restaurant.getOrderDirectory().createNewOrder(customer);
        for(FoodItem fo : cart)
        {
            order.addItem(fo);
        }
        order.calculateTotalAmount();
        JOptionPane.showMessageDialog(null, "Thank you for your order! Order of " + order.getFoodItemList().size() + " food item(s) for amount $" + order.getTotalAmount() + " is placed successfully!");
    }//GEN-LAST:event_btnOrderActionPerformed

